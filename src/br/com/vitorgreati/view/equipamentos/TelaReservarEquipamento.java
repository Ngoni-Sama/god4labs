    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        Agendamento agendamento = this.agendamentos.get(tbReservas.getSelectedRow());
        if(!agendamento.getUsuario().getUsername().equals(SessaoUsuario.getUsuario().getUsername())){
            JOptionPane.showMessageDialog(null, "Você só pode cancelar as suas reservas.", "Erro", JOptionPane.ERROR_MESSAGE);
        }else {
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja mesmo cancelar essa reserva?", "Confirmar exclusão", 
                    JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(opcao==0){
                agendamentoDAO.excluir(agendamento);
                JOptionPane.showMessageDialog(null, "Agendamento cancelado.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.agendamentos.remove(tbReservas.getSelectedRow());
                DefaultTableModel model =(DefaultTableModel) tbReservas.getModel();
                model.removeRow(tbReservas.getSelectedRow());
            }        // TODO add your handling code here:
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

