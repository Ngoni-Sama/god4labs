    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Date dataInicio = (Date) snDe.getModel().getValue();
        Date dataFinal = (Date) snAte.getModel().getValue();
        String descricao = taDescricao.getText();
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        
        if(format.format(dataInicio).equals(format.format(dataFinal))){
            JOptionPane.showMessageDialog(null, "As datas não podem ser iguais.", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }else{
            boolean agendamentoPermitido = true;
            for(Agendamento a : agendamentos){
                if((dataInicio.after(a.getDataInicio()) && dataInicio.before(a.getDataFinal())) || 
                        (dataFinal.after(a.getDataInicio()) && dataFinal.before(a.getDataFinal()))){
                    agendamentoPermitido = false;
                }
            }

            if(agendamentoPermitido){
                Agendamento agendamento = new Agendamento(descricao,dataInicio,dataFinal,SessaoUsuario.getUsuario(),this.equipamento);
                agendamentoDAO.cadastrar(agendamento);
                JOptionPane.showMessageDialog(null, "O equipamento foi agendado.", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                frameInvocador.setEnabled(true);
                dispose();
                //preencherTabela();
            }else{
                JOptionPane.showMessageDialog(null, "O equipamento já está agendado para esse período.", "Erro ao agendar", JOptionPane.ERROR_MESSAGE);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

