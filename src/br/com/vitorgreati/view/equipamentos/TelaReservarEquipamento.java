    private void preencherTabela(){
        this.agendamentos = agendamentoDAO.listarPorEquipamento(this.equipamento);
        DefaultTableModel model = (DefaultTableModel) tbReservas.getModel();
        model.setRowCount(0);
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        for(Agendamento a : agendamentos){
            model.addRow(new String[]{format.format(a.getDataInicio()),format.format(a.getDataFinal()),a.getUsuario().getUsername()});
        }
        tbReservas.setModel(model);
    }

